package org.example.core;

public class Service {

    public static int add(int a, int b){
        return a + b;
    }

    public static int sum(int a, int b){
        return a * b;
    }

    public static int sub(int a, int b){
        return a - b;
    }

    public static int dif(int a, int b){
        return a / b;
    }

}
